"""
qq号码
"""
# 1.定义一个变量记录QQ号码
qq_number = "1234567"

# 2.定义一个变量记录QQ密码
qq_password = "123"

# 注意：在使用解释器执行Python程序的时候，不能直接使用变量名
# 在控制台输出变量的信息
qq_number

# 如果希望通过解释器的方式输出变量的内容，需要使用print函数
print(qq_number)
print(qq_password)

"""
超市买苹果
"""
# 1.定义苹果的单价
price = 8.5

# 2.挑选苹果
weight = 7.5

# 3.计算苹果金额
money = weight * price

# 4.只要买苹果，就返回5块钱
# 变量名只有在第一次出现才叫定义变量
# 变量名再次出现，不是定义变量，而是直接使用之前定义过的变量
money = money - 5

print(money)

